// Callback function là một hàm được truyền vào một hàm khác như 
// một đối số và sau đó được gọi lại sau khi hàm gốc hoàn thành 
// công việc của mình. Trong JavaScript, callback function thường 
// được sử dụng để xử lý bất đồng bộ, như khi gửi yêu cầu AJAX, xử 
// lý sự kiện, hoặc thực hiện các thao tác không đồng bộ khác.



// Chức năng của callback function là để đảm bảo rằng một phần cụ 
// thể của mã được thực hiện sau khi một tác vụ khác hoàn thành. 
// Điều này giúp đảm bảo rằng mã của bạn không bị chặn khi thực 
// hiện các tác vụ không đồng bộ.



// Cách callback function hoạt động trong JavaScript là khi một 
// hàm cần thực hiện một công việc không đồng bộ, nó sẽ chấp nhận 
// một callback function làm đối số. Khi công việc đó hoàn thành, 
// hàm gốc sẽ gọi lại callback function để xử lý kết quả. Điều này
// cho phép bạn thực hiện các tác vụ không đồng bộ mà không cần 
// chặn mã của bạn và chờ đợi cho đến khi tác vụ hoàn thành.

